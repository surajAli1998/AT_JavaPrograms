/**
 * concept: Format specifier
 */
public class Lab004 {
    public static void main(String[] args) {
//        %d ->  any integer - byte, short, int or long
//        %s -> String
//        %c -> char
//        %f -> float

        int age = 26;
        System.out.printf("I am Suraj and my age is %d\n",age);

        String str = "ATB6X";
        System.out.printf("Current ongoing course is %s",str);
    }
}
